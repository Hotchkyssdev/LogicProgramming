#include <stdio.h>

int main() {
    int entero = 10;
    float flotante = 5.5;
    char caracter = 'A';
    double doble = 20.99;
    printf("Entero: %d\n", entero);
    printf("Flotante: %f\n", flotante);
    printf("Caracter: %c\n", caracter);
    printf("Doble: %lf\n", doble);
    return 0;
}